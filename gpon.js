// Диагностика для Сибирских филиалов:
// - все MA4000-конфиги используют диагностику MA4000 (Урал)
// - LTP-конфиги используют диагностику LTP (Урал)
const uralMa4000Diag = window.gponDevices.eltex_ma4000?.diagnostics || {};
const uralLtpDiag = window.gponDevices.eltex_ltp?.diagnostics || {};

Object.keys(window.gponDevices).forEach((key) => {
  if (!key.startsWith('eltex_')) return;
  if (window.gponDevices[key].diagnostics) return;
  window.gponDevices[key].diagnostics = key.startsWith('eltex_ltp_')
    ? uralLtpDiag
    : uralMa4000Diag;
});
